void GameSelectKey()
{

	char presskey;

	//키가 입력되면 잡아준다.
	if (_kbhit())
	{
		presskey = _getch();

		switch (presskey)
		{
		case 72:
			Player_Select--;
			break;
		case 32:
			Select = 1;
			break;
		case 80:
			Player_Select++;
			break;

		}

	}

}


